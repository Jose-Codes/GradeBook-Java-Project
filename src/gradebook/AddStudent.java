    public static void populateArrayList(){
    try{
          FileInputStream file = new FileInputStream("Classes.dat");
          ObjectInputStream inputFile = new ObjectInputStream(file);
          
          boolean endOfFile = false;
          while(!endOfFile){
              try{
                  classes.add((Class) inputFile.readObject());
                  
          }
          catch(EOFException e){
                endOfFile = true;
          }
              catch(Exception f){
                  JOptionPane.showMessageDialog(null, f.getMessage());
              }
          }
          inputFile.close();
        }
        catch(IOException e){
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    try{
        FileInputStream file2 = new FileInputStream("Students.dat");
        ObjectInputStream inputfile2 = new ObjectInputStream(file2);
        
        boolean endOfFile = false;
        while(!endOfFile){
        try{
            students.add((Student) inputfile2.readObject());
        }
        catch(EOFException e){
            endOfFile = true;
        }
        catch(Exception x){
            JOptionPane.showMessageDialog(null, x.getMessage());
        }
        }
        inputfile2.close();
        }
        catch(IOException e){
        JOptionPane.showMessageDialog(null, e.getMessage());
    }   
    }
    
    public void saveStudentsToFile(){
        try{
        FileOutputStream file = new FileOutputStream("Students.dat");
        ObjectOutputStream outputFile = new ObjectOutputStream(file);
        for(int i =0; i<students.size();i++){
            outputFile.writeObject(students.get(i));
        }
        outputFile.close();
        JOptionPane.showMessageDialog(null, "Student was added");
        this.dispose();
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
