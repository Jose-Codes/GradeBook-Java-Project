    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(firstName.getText().isEmpty() ||lastName.getText().isEmpty()||grade.getText().isEmpty()||ID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter all fields!");
        }
        else{
           String first = firstName.getText().trim();
           String last = lastName.getText().trim();
           int Grade = Integer.parseInt(grade.getText().trim());
           int IDnumber = Integer.parseInt(ID.getText().trim());
           int classIndex = jComboBox1.getSelectedIndex();
           Class class1 = classes.get(classIndex);
           Student student = new Student(first,last,class1,Grade,IDnumber);
           students.add(student);
           saveStudentsToFile();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

